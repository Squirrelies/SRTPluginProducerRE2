        public static GameVersion DetectVersion(string? filePath)
        {
            if (string.IsNullOrWhiteSpace(filePath))
                return GameVersion.Unknown;

            byte[] checksum;
            using (SHA256 hashFunc = SHA256.Create())
            using (FileStream fs = new FileStream(filePath, FileMode.Open, FileAccess.Read, FileShare.ReadWrite | FileShare.Delete))
                checksum = hashFunc.ComputeHash(fs);

            if (checksum.SequenceEqual(re2WW_20220613_1))
                return GameVersion.RE2_WW_20220613_1;
            else if (checksum.SequenceEqual(re2WW_20211217_1))
                return GameVersion.RE2_WW_20211217_1;
            else if (checksum.SequenceEqual(re2WW_20210201_1))
                return GameVersion.RE2_WW_20210201_1;
            else if (checksum.SequenceEqual(re2CeroZ_20210201_1))
                return GameVersion.RE2_CEROZ_20210201_1;
            else if (checksum.SequenceEqual(re2WW_20200718_1))
                return GameVersion.RE2_WW_20200718_1;
            else
                return GameVersion.Unknown;
        }
